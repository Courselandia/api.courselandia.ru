namespace App\Modules\Employment\Http\Requests\Admin;

use App\Models\FormRequest;

/**
 * Класс запрос для создания трудоустройства.
 */
class EmploymentCreateRequest extends FormRequest
{
    /**
     * Возвращает правила проверки.
     *
     * @return array Массив правил проверки.
     */
    public function rules(): array
    {
        return [
            'status' => 'boolean',
        ];
    }

    /**
     * Возвращает атрибуты.
     *
     * @return array Массив атрибутов.
     */
    public function attributes(): array
    {
        return [
            'status' => trans('employment::http.requests.admin.employmentCreateRequest.status')
        ];
    }
}
