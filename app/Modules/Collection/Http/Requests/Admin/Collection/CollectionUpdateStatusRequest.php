 */

namespace App\Modules\Collection\Http\Requests\Admin\Collection;

use App\Models\FormRequest;

/**
 * Класс запрос для обновления статуса коллекции.
 */
class CollectionUpdateStatusRequest extends FormRequest
{
    /**
     * Возвращает правила проверки.
     *
     * @return array Массив правил проверки.
     */
    public function rules(): array
    {
        return [
            'status' => 'required|boolean',
        ];
    }

    /**
     * Возвращает атрибуты.
     *
     * @return array Массив атрибутов.
     */
    public function attributes(): array
    {
        return [
            'status' => trans('collection::http.requests.admin.collection.collectionUpdateStatusRequest.status'),
        ];
    }
}
